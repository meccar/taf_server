namespace Shared.Constants;

/// <summary>
/// Provides commonly used date and time format constants.
/// </summary>
public class TimerConstants
{
    /// <summary>
    /// Represents the datetime format as "YYYY-MM-DD HH:mm:ss".
    /// </summary>
    public static readonly string FormatDatetime = "YYYY-MM-DD HH:mm:ss";

    /// <summary>
    /// Represents the time format as "HH:mm:ss".
    /// </summary>
    public static readonly string FormatTime = "HH:mm:ss";

    /// <summary>
    /// Represents the date format as "MM/DD/YYYY".
    /// </summary>
    public static readonly string FormatDate = "MM/DD/YYYY";
}